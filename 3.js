var colours = ["Red,Green,Blue"];
alert("Colours : " + colours);
document.write(colours + "<br/>");
var addColours = prompt("Which Colour do you want to add at the begining?");
colours.unshift(addColours);
alert(colours);
document.write(colours + "<br/>");
var addColoursEnd = prompt("Which Colour do you want to add at the end");
colours.push(addColoursEnd);
alert(colours);
document.write(colours + "<br/>");
var updateColour = +prompt("At which index do you want to add colour");
var newColour = prompt("Enter the colour you want to add at specific index?");
colours.splice(updateColour , 0 , newColour);
alert(colours);
document.write(colours + "<br/>");
var deleteColour = prompt("At which index do you want to delete colour?");
var coloursDeleted = prompt("How many colours do you want to delete on that index?");
colours.splice(deleteColour , --coloursDeleted);
alert(colours);
document.write(colours + "<br/>");